let html = document.querySelector('#materiales');
let response = await fetch('../Pages/materiales.html');
if (!response.ok) {
  throw new Error(`HTTP error! status: ${response.status}`);
} else {
  let mytext = await response.text();

  html.innerHTML = mytext
}


